#!/bin/bash

# Устанавливаем размер терминала: 100 столбцов и 20 строк для xterm
# Команда \033[8;20;100t изменяет размер окна терминала
echo -e "\033[8;20;100t"

# Очищаем экран перед началом выполнения скрипта
clear

# Цветовая палитра для текста (неоновый красный)
readonly COLOR='\033[38;5;196m'  # Неоновый красный цвет для текста
readonly RESET_COLOR='\033[0m'   # Сброс цвета, чтобы вернуть стандартный цвет

# Функция для анимации текста с плавным эффектом
# text - текст, который будет отображаться
# delay - задержка между выводом каждого символа в секундах
animate_text() {
    local text="$1"  # Текст, который будет анимирован
    local delay="$2"  # Задержка между выводом символов

    # Печать текста по одному символу с заданной задержкой
    for ((i=0; i<${#text}; i++)); do
        # Выводим каждый символ с заданным цветом и сбрасываем цвет после
        echo -n -e "${COLOR}${text:i:1}${RESET_COLOR}"
        # Устанавливаем задержку для плавного отображения
        sleep "$delay"
    done
    # Печатаем новую строку после завершения анимации
    echo
}

# ASCII-арт текст "DevCicadaY" в виде многострочной строки
dev_cicada_text="
########  ######## ##     ##  ######  ####  ######     ###    ########     ###    ##    ## 
##     ## ##       ##     ## ##    ##  ##  ##    ##   ## ##   ##     ##   ## ##    ##  ##  
##     ## ##       ##     ## ##        ##  ##        ##   ##  ##     ##  ##   ##    ####   
##     ## ######   ##     ## ##        ##  ##       ##     ## ##     ## ##     ##    ##    
##     ## ##        ##   ##  ##        ##  ##       ######### ##     ## #########    ##    
##     ## ##         ## ##   ##    ##  ##  ##    ## ##     ## ##     ## ##     ##    ##    
########  ########    ###     ######  ####  ######  ##     ## ########  ##     ##    ##    
"

# Анимация текста с плавным выводом символов
# Задержка 0.00 секунды для мгновенного вывода
animate_text "$dev_cicada_text" 0.00  # Задержка 0.00 секунды для плавного эффекта